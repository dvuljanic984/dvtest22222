				<h4>EVENTS COMPONENT</h4>
				<p>Events component was designed following the customer’s instructions when Jooma CMS conventional abilities were not enough. The component is able to create an „event“, containing the name, beginning time, ending time, location with an interactive map that can lead you to the location, description, generation of links to social networks (facebook, twitter, google+), as well as the data on the organiser. The component also has a built-in ability to generate a link where you can get the iCal format. It also possesses the option of repeating particular events in certain intervals, which makes it highly useful, especially when an event is repeated weekly or monthly.</p>
