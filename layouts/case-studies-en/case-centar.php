				<h4>RESPONSIVE DESIGN</h4>

				<p>In preparing and developing the design we kept in mind the fact that the site should be able to adapt to any device. This is why we used the „Mobile First“ approach in site development. This is a development technique where primary focus is small screen (most often cellphone) and contents are step by step adjusted to bigger screens.</p>
				<p>The menu is hidden to save the precious space and is activated by a "hamburger menu" icon (☰). To use the area optimally we also decided to remove the pictures when viewing the news on the home page.</p>
				<p>Calendar of events was a challenge, due to the amount of the information presented. The filters are hidden in the menu and called by selecting the option „Filters“, while the presentation of the events is always turned to the present day. We have also left small parts of the previous and next day, just to stimulate the user to „swipe“ and show him that he can scroll the days by simply moving the finger left/right on the screen. If the user notices this function, there are arrows provided to scroll left/right, so no mistake is possible.</p>
