				<h4>YOUTH CENTER</h4>

				<div class="text-left">
					<p>The project we did for CarpeDiem (Association for the Promotion and Development of Creative and Special Potential for Children, Youth and Adults) was a source of great enjoyment for us, as it directly impacted the level of available information for our fellow citizens.</p>
					<p>We had general guidelines after 2 or 3 shorter meetings – the site should be of vivid colours, loaded with information, easily accessible and, what is the most important, responsive, as it was expected to be mostly approached by young people from their cellphones.</p>
					<p>Having this in mind, we agreed to develop the project as a double-sided one, with 2 designers, 2 drafts, 2 unique designs, in a single project and with a single happy customer. After the first interaction with the customer, we already had a solution that was accepted by both sides. We even had a nick-name for the design we both accepted, calling it a „forest“.</p>
					<p>We developed and perfected some details and functions in constant communication with our customer. Intensive testing of the site on various devices (yes, we even used some „ancient“ devices) resulted in relatively quick and successful completion of the project.</p>
