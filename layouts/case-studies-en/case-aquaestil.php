				<h4>DISTRIBUTORS</h4>


				<p>A component was developed to enable input and presentation of all Aquestil distributors at an interactive map. The administrator's part of the component offers adding new and deleting nonexistent distributors in a simple and quick way.</p>
				<p>The map has the ability of filtering by state and/or city. City filtering depends on the selected state, which makes using and filtering distributers easier.</p>
				<p>The component also includes the Google GeoCoding, so that it is not necessary to enter manually for each distributor geographic longitude and latitude to be presented at the map.</p>

