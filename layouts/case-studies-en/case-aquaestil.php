				<h4>PRODUCTS</h4>

				<p>We constructed the whole component to be used for the input of Aquestil assortment, with 7 categories and subcategories included. Each product is supposed to have all the multilingual descriptions, names, etc. It was not easy to construct the data basis for this component. Products from different categories differ, of course, but they are also quite similar sometimes, and it took us some time to find the best relationship of tables and fields, to optimise the basis maximally and again to make it maximally functional as well.</p>
				<p>We had to apply automatic picture processing in the component itself, as there were a huge number of products involved, and even more of their pictures (of which many were not optimised).</p>
				<p>Besides entering the products, we had to link them together, as some of them could be combined, e.g. showers and partitions.</p>


