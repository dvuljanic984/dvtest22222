				<h4>General description</h4>

				<div class="text-left">
					<p>This project was a real challenge. Even after the first technical meeting it was quite evident what direction we were going to take and we got green light to use modern technologies. Our task was to make something that would be a just „Wow“.</p>

					<p>We first filled in our drawing board with everything we had in mind, drawing and blotting schemes and good ideas. At the end we decided to do a double-sided design. Two designers were included, with two unique designs, both for a single project.</p>
					<p>After a week of work and a bunch of internal ideas we were faced with two design proposals, which we decided to present to our customer. The customer liked both designs, each of them had something special and attractive, so we decided, with the approval of our customers, to take the best from both proposals and construct a third design. Finally, the customer was sincerely satisfied and his comment was: „This is it! Good job!"</p>
					<p>In developing the site we implemented modern technologies, such that mark the future in the business. All the graphic presentations and icons were SVG, so as to make site loading faster and to enable animations.</p>
					<p>Apart from design, quite a challenge was to construct a top-quality user interface to be used to administer the whole data basis for all the products that were supposed to be presented at the site.</p>
