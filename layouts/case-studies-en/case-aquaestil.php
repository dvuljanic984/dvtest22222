				<h4>Responsive web-site design </h4>

				<p>In preparing and completing the design we constantly had in mind the requirement for the site to be able to adapt to all devices. Aware of this, we used „Mobile First“ approach in developing the site. This is a development technique where primary focus is small screen (most often cellphone) and contents are step by step adjusted to bigger screens.</p>
				<p>Key challenge was to present a multitude of products with subcategories at a small screen. As the product photos were of very high quality, we decided to present a single product in a line on conventional small screens, while the number of products increased with the increased size of the screen.</p>
				<p>The main menu in the so called "mobile version“ is hidden and is activated by clicking the "hamburger menu" icon (☰). The items in the main menu that contain submenus are designated with a „+“ sign, so as to suggest to the user the position of a submenu.</p>



