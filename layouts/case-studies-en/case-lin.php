				<h4>General description</h4>


				<div class="text-left">

					<p>Lin's store has been for years offering a wide range of various products at its shops. Their previous web store entitled "svezakucuidom.com" was outdated both by design and functionality. The company was looking for a new and fresh appearance, new functions and a responsive site, so as to improve the experience of existing and new customers alike. The old system could not follow their newly developed needs and the company decided to redesign the site and adopt a safer and better system, able to be developed while running. This is why we suggested the Magento CE system, widely used and with good results.</p>
					<p>At the start we encountered numerous challenges, such as „scattered“ existing bases for over 50 000 products, divided into 450 categories. In transferring the old bases into the new system we have reorganised categories and meaningfully reduced the number of categories by ~ 50.</p>
					<p>The new system was also supposed to enable easier new product entry, multicategorising the products, simplification of the overall process, from ordering to selling, both at the site for the use of the customers and for the administration in the system.</p>
					<p>Magento CE enabled us, with slight modifications and additions, to achieve these goals. Besides conventional functions, we also added fiscalisation, constructed special modules for the purpose of presenting particular product groups (for example, modules for the newest and best-sell products), and installed the WSPaypaymentgateway. An important item was also updating the amount of the product on store and their prices, for which we constructed a special module where the administrator could easily update both sets of data.</p>
				</div>
