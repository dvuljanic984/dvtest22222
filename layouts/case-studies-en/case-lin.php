				<h4>Responsive design</h4>

				<p>It was not easy to develop a mobile application for a web shop with over 50 000 articles. We had to adjust the system and enable the user to get a simple and intuitive presentation of the articles, offer connecting onto the system, overview of the shopping basket and articles to be compared and, the most important, to design the ordering procedure. We decided to adopt the approach of fixed navigation, so as to enable the customer to approach all the categories at every moment, as well as to approach the key elements, such as shopping basket overview, profile view, articles for comparison and the ability to register with the system.</p>
				<p>Mobile presentation "hides" all the secondary actions that could divert the attention of the customer in reviewing the articles. However, they can be seen by a simple click on particular elements, such as product filters. Employing this approach, the user can search through the article without any disturbance, and call for the filters, sorting etc. when needed.</p>
				<p>Ordering process is simplified to be adapted to the smaller user area available on mobile devices.</p>
