				<h4>RESPONSIVE DESIGN</h4>

				<p>In preparing and developing the design we kept in mind the fact that the site should be able to adapt to any device. This is why we used the „Mobile First“ approach in site development. This is a development technique where primary focus is small screen (most often cellphone) and contents are step by step adjusted to bigger screens.</p>
				<p>The main menu on „Mobile version“ is hidden and is activated using the well known "hamburger menu" icon (☰). Page header includes the menu icon, while logo and browsing can be seen on the mobile display when the user starts returning towards the page top. Header is thus not in the way and does not occupy precious space on the device with small screens (mostly cellphones) offering the user at the same time the possibility to open the menu or browsing in a quick and easy way.</p>
				<p>Due to the type of the project in question, most of the data were shown in a table form, which presented a problem. Having in mind our experience from the previous projects, feedback information and consultations with our customer, we decided to leave the tables in their full width and not to rearrange them vertically. We also installed indicators that showed the user how to review all the data in the table, not seen at the moment, by simply moving his finger left/right on the screen.</p>
