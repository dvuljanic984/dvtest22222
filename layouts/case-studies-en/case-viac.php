				<h4>General description</h4>

				<div class="text-left">

					<p>It was, definitely, quite a special project. Apart from the fact that we had to develop a functional site on a short notice, in just a month and a half, it was also our first international project. The task was to develop a site which would help the organisation to find necessary volunteers, as well as to enable communication among volunteers. The most serious challenge at the beginning was to find the way to realise a permanent communications with customer and decide which tools could be used for the purpose. To achieve communication in design creation, we selected the cloud solution UXPin. UXPin enabled us to get a continuous feedback from our customer and helped us to complete the design in a short time period we had at our disposal, which made the customer highly happy and satisfied, despite the fact that we were 700 km apart.</p>
					<p>Besides design and communications, the challenge facing us was also to programme all the functions and possibilities of the site in a short time, as well as to test it prior to production. The site also had to contain a component for browsing and for updating educational programmes, as well as all of its details. The biggest component of the site is connected with data basis of the volunteers. We had to develop an administrator's part to update it, together with the front part that the volunteers could use to communicate among themselves, with the organisations searching for the volunteers and channels to communicate with these organisations.</p>
