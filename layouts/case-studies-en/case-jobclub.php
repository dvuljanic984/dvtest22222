				<h4>General description</h4>

				<div class="text-left">

					<p>The association CARPE DIEM from Karlovac started the project entitled “JOB CLUB FOR YOUTH", and decided we could be their partners in it. The project was financed through EU funds, thus we were obliged to follow certain rules from the very start.</p>
					<p>The project was quite specific as a part of the site, dedicated to general public, was a simple single page web, while the rest was supposed to be a full portal or a kind of mini web application. Project leaders were supposed, using this part of the project, to guide all the users and process information on them, while project users could use it to access necessary information and data, essential for them to be able to participate in the project.</p>
					<p>Besides project leaders and attendants (users), the application was also used by the employers and the HZZ (Croatian Employment Service). Employers were able to obtain a review of the attendants, their CVs and video CVs. The HZZ used a part of the application to keep the record of the attendant consultations and it took the attendants under its care after their education in the Youth Centre was completed.</p>

				</div>


				<div class="row">
					<div class="small-12 medium-4 columns">
						<h6>GENERAL AND SPECIFIC GOALS OF THE PROJECT "JOB CLUB FOR YOUTH":</h6>
						<p>
							General goal was to reduce the number of unemployed young people in the town of Karlovac and increase their social inclusion, while specific goal was to develop services for unemployed young people, through which they could attain skills in looking for job and additional knowledge necessary at the labour market.
						</p></div>
					<div class="small-12 medium-4 columns">
						<h6>MAIN TARGET GROUP:</h6>
						<p> 96 unemployed young people in Karlovac.</p></div>
					<div class="small-12 medium-4 columns">
						<h6>THE PROJECT WAS PLANNED TO YIELD THE FOLLOWING RESULTS: </h6>
						<p>Three representatives of civil society were previously educated to work with the young unemployed people, the Club plan for the employment was developed and applied, 96 young people learned new skills in looking for a job, at least 10 experts-volunteers were involved in the work with young unemployed people and a service was established aimed at better connections between job seekers and employers.
						</p>

