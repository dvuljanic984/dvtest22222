				<h4>Responzivni dizajn</h4>
				<p>Prilikom pripreme i izrade samog designa, imali smo na umu da se stranica mora prilagoditi svim uređajima. Svjesni te činjenice, koristili smo "Mobile First" pristup kod razvoja stranice. To je tehnika razvoja gdje je primarni
					fokus mali ekrani (ugl. mobitel) te se postepeno sadržaj prilagodava većim ekranima.</p>
				<p>Kod prilagodbe stranice mobilnim uređajima, znali smo da je fokus stranice postojeći ili budući sudionik projekta te smo u pri plan stavili obrazac za spajanje ili registraciju.</p>
				<p>Glavni izbornik na "mobilnoj verziji" je sakriven, te se aktivira na dobro poznati "hamburger menu" ikonu (&#9776;).</p>
				<p>Obzirom na vrstu projekta, većina podataka je prikazana u tabličnom prikazu što je predstavljalo problem. Nakon razgovora s članovima udruge i već tada postojećim sudionicima, došli smo do zaključla da je bolje ostaviti tablice
				u punoj širini i ne preslagivati ih vertikalno. Postavili smo indikatore koji sugeriraju korisniku da pomakom prsta lijevo/desno može pregledati sve podatke u tablici koji u tom trenutku nisu vidlljivi na malom ekranu.</p>
				<p>Kod prikaza materijala koji su polaznici mogli preuzimati, koristili smo "Accordion" pristup.</p>
