				<p>Prilikom pripreme i izrade samog dizajna, imali smo na umu da se stranica mora prilagoditi svim
					uređajima. Svjesni te činjenice, koristili smo "Mobile First" pristup kod razvoja stranice. To je
					tehnika razvoja gdje je primarni fokus mali ekrani (ugl. mobitel) te se postupno sadržaj prilagodava
					većim ekranima.</p>
				<p>Izbornik je sakriven kako ne bi oduzimao dragocjeni prostor te se aktivira na "hamburger menu" ikonu
