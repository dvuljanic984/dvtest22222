				<p>Izrađena je komponenta za događanja prema naputcima klijenta, tamo gdje standardne mogućnosti Joomla CMS-a
					nisu bile dovoljne. Komponenta ima mogućnost kreirati "događaj" koji sadrži ime, vrijeme početka, vrijeme
					završetka, lokaciju s interaktivnom kartom preko koje je moguće odrediti položaj, opis, generiranja poveznica
					prema društvenim mrežama (facebook, twitter, google+) te podatke o organizatoru. U komponentu je ugrađena i mogućnost
					generiranja link nakojem se može dobiti iCal format.
					Komponeneta također ima opciju da se određeni događaj ponavlja u određenim intervalima, što ju čini vrlo korisnom pogotovo kad se neki događaj
