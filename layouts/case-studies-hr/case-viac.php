				<h4>Responzivni dizajn</h4>
				<p>Prilikom pripreme i izrade samog designa, imali smo na umu da se stranica mora prilagoditi svim uređajima. Svjesni te činjenice, koristili smo "Mobile First" pristup kod razvoja stranice. To je tehnika razvoja gdje je primarni
					fokus mali ekrani (ugl. mobitel) te se postepeno sadržaj prilagodava većim ekranima.</p>
				<p>Glavni izbornik na "mobilnoj verziji" je sakriven, te se aktivira na dobro poznati "hamburger menu" ikonu (&#9776;). Zaglavlje stranice koje uključuje ikonu za izbornik, logotip te pretraživanje se na mobilnom prikazu pokazuje
					kada se korisnik krene vraćati prema vrhu stranice. Na taj način zaglavlje ne smeta i ne zauzima dragocjeni prostor na uređajima s malim ekranima (ugl. mobiteli) a opet korisniku daje mogucnost da otvori izbornik ili
					pretraživanje na brz i laki način.</p>
				<p>Obzirom na vrstu projekta, većina podataka je prikazana u tabličnom prikazu što je predstavljalo problem. Obzirom na iskustva iz prijašnjih projekata, povratnih informacija i razgovora s klijentom, odlučili smo ostaviti tablice u punoj širini i ne preslagivati ih vertikalno. Postavili smo indikatore koji sugeriraju korisniku da pomakom prsta lijevo/desno može pregledati sve podatke u tablici koji u tom trenutku nisu vidljivi na malom
					ekranu.</p>
