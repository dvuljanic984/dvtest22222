				<div class="text-left">
					<p>
						Definitivno izuzetno poseban projekt. Pored činjenice da je trebalo napravit funkcionalni portal u vrlo kratkom roku od samo mjesec i pol dana, ovo je bio naš prvi međunarodni projekt. Zadatak je bio napraviti portal na kojemu će organizacije moći pronaći potrebne volontere, ta omogućiti komunikaciju među volonterima. Najveći izazov u početku je bio kako realizirati konstantnu komunikaciju sa klijentom i koje alate koristit za te potrebe. Za komunikaciju pri izradi dizajna, odabrali smo cloud rješenje  UXPin. UXPin nam je omogućio kontinuirani feedback od strane klijenta, tako da smo uspijeli u kratkom roku odraditi kompletni dizajn sa kojim je klijent bio jako zadovoljan, unatoč činjenici da nas dijeli preko 700Km.					</p>
					<p>
						Osim dizajna i komunikacije, izazov je bilo u kratkom roku isprogramirati sve funkcije i mogućnosti portala, te odraditi testiranje prije puštanja u produkciju. Portal je morao sačinjavati komponentu za pretraživanje i ažuriranje edukacijskih programa, te detalja istih. Najveća komponenta na portalu je dio vezan uz bazu podataka volontera, za koju je trebalo napraviti administracijski dio za ažuriranje iste, te front dio koji bi koristili volonteri za međusobnu komunikaciju, te organizacije za pronalazak volontera i komunikaciju sa njima.
